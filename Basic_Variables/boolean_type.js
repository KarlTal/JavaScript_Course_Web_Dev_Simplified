//Boolean -> true or false

let a = true;
let b = false;

console.log(a);
console.log(b);

console.log(typeof a);

//Special operators -> && (AND) || (OR)

console.log(true && false);
console.log(true || true);

//Two Variables
let happy = true;
let fun = true;

console.log(happy && fun);

//Invert
console.log(!happy);
console.log(!!happy);
