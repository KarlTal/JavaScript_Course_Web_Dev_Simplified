//Number comparisons

let a = 1;
let b = 2;

//Equal to -> ==
console.log(a == b); //Check if a = b

//Not equal to -> !=
console.log(a != b);

//Less than -> <
console.log(a < b);

//Greater than -> >
console.log(a > b);

//String comparisons

let stringOne = "a";
let stringTwo = "b";

console.log(stringOne == stringTwo);
console.log(stringOne != stringTwo);

//Checking if stringOne is less/greater than stringTwo
console.log(stringOne > stringTwo); //Checks the first character whether it is greater in the alphabet.

//Null and undefined comparisons
let aNull = null;
let bUndefined = undefined;

console.log(aNull == bUndefined);
console.log(aNull != bUndefined);

//Strict equality -> ===
let string1 = "1";
let number = 1;
console.log(string1 === number);
